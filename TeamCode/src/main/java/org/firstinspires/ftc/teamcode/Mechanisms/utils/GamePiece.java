package org.firstinspires.ftc.teamcode.Mechanisms.utils;

/**
 * The GamePiece class provides a type-safe representation of game elements,
 * specifically their colors.
 */
public class GamePiece {

    /**
     * Defines the possible colors of a game piece.
     */
    public enum Color {
        GREEN,
        PURPLE,
        NONE // Represents an empty slot
    }
}
